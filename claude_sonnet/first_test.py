# Описываем ситуацию за покерным столом
имя_игрока = "Алексей"
количество_фишек = 500

# Карты у игрока (первая и вторая)
масть_первой_карты = "пики"
значение_первой_карты = "туз"

масть_второй_карты = "пики"
значение_второй_карты = "король"

# Выводим информацию об игроке
print(f"Игрок: {имя_игрока}")
print(f"Фишки: {количество_фишек}")
print(f"Карты: {значение_первой_карты} {масть_первой_карты} и {значение_второй_карты} {масть_второй_карты}")

# Проверяем, есть ли у игрока туз
есть_туз = значение_первой_карты == "туз" or значение_второй_карты == "туз"
print(f"У игрока есть туз? {есть_туз}")

# Проверяем, одной ли масти карты (для возможного флеша)
одна_масть = масть_первой_карты == масть_второй_карты
print(f"Карты одной масти? {одна_масть}")
